# Arithmetic Operators
a = 10
b = 3
print("Arithmetic Operators:")
print("Addition:", a + b)      # Output: 13
print("Subtraction:", a - b)   # Output: 7
print("Multiplication:", a * b) # Output: 30
print("Division:", a / b)       # Output: 3.3333333333333335
print("Floor Division:", a // b) # Output: 3
print("Modulus:", a % b)        # Output: 1
print("Exponentiation:", a ** b) # Output: 1000
print()

# Comparison Operators
x = 10
y = 5
print("Comparison Operators:")
print("Equal to:", x == y)         # Output: False
print("Not equal to:", x != y)     # Output: True
print("Greater than:", x > y)      # Output: True
print("Less than:", x < y)         # Output: False
print("Greater than or equal to:", x >= y) # Output: True
print("Less than or equal to:", x <= y)    # Output: False
print()

# Logical Operators
p = True
q = False
print("Logical Operators:")
print("Logical AND:", p and q) # Output: False
print("Logical OR:", p or q)   # Output: True
print("Logical NOT:", not p)   # Output: False
print()

# Bitwise Operators
a = 5   # Binary: 101
b = 3   # Binary: 011
print("Bitwise Operators:")
print("Bitwise AND:", a & b)   # Output: 1 (Binary: 001)
print("Bitwise OR:", a | b)    # Output: 7 (Binary: 111)
print("Bitwise XOR:", a ^ b)   # Output: 6 (Binary: 110)
print("Bitwise NOT:", ~a)      # Output: -6 (Binary: ...11111010)
print("Left shift:", a << 1)   # Output: 10 (Binary: 1010)
print("Right shift:", b >> 1)  # Output: 1 (Binary: 001)
